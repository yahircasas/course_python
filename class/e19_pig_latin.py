"""
Pig Latin is a common children’s “secret” language in English-speaking countries.
The rules for translating words from English into Pig Latin are quite simple:
    * If the word begins with a vowel (a, e, i, o, or u), add “way” to the end of the
        word. So “air” becomes “airway” and “eat” becomes “eatway.”
    * If the word begins with any other letter, then we take the first letter, put it on
        the end of the word, and then add “ay.” Thus, “python” becomes “ythonpay”
        and “computer” becomes “omputercay.”
(The rules can be made more sophisticated. Let’s keep it simple for the purposes of this exercise.)
For this exercise, write a Python function (pig_latin) that takes a string as input,
assumed to be an English word. The function should return the translation of this word
into Pig Latin.
"""

def pig_latin(palabra):
    vocales = 'aeiou'
    if palabra[0].lower() in vocales:
        return palabra + 'way'
    else:
        return palabra[1:] + palabra[0] + 'ay'

# Solicitando al usuario que ingrese una palabra
entrada = input("Ingrese una palabra en inglés: ")
print(pig_latin(entrada))
